myFifths = (".2", ".4", ".6", ".8", ".0")

subUnitOneFifth = [
    "Mauritania", "Madagascar"
    ]
def roundToFifth():
    global bill_amount
    global country
    resolution = 0.2
    if country  in subUnitOneFifth and str(bill_amount).endswith(myFifths) == False:
        print((round (bill_amount / resolution) * resolution))
        print("1/5 function")
    else:
        print("do not")

def oneSf():
    global bill_amount
    bill_amount = format(bill_amount, '.1f')

def twoSf():
    global bill_amount
    global country
    if country not in subUnitOneFifth:
        bill_amount = format(bill_amount, '.2f')
    else:
        oneSf()
        roundToFifth()

